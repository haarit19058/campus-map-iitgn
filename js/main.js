// Function to search hostels by name
function searchHostel() {
    const searchQuery = document.getElementById("search").value.toLowerCase();
    let found = false;

    // Iterate through each hostel
    hostels.forEach(hostel => {
        const hostelName = hostel.features[0].properties.name.toLowerCase();

        if (hostelName.includes(searchQuery)) {
            found = true;

            // Get the corresponding GeoJSON layer for the hostel
            const layer = L.geoJSON(hostel, {
                onEachFeature: function (feature, layer) {
                    if (feature.properties && feature.properties.name) {
                        layer.bindPopup(feature.properties.name);
                    }
                }
            });

            // Zoom into the bounds of the hostel area
            map.fitBounds(layer.getBounds());

            // Open the popup for the matched hostel
            layer.openPopup();
        }
    });

    // If no hostel is found, display an alert
    if (!found) {
        alert("Hostel not found!");
    }
}

